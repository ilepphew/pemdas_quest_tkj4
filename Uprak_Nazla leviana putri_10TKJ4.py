# buatlah sintaks menghitung volume bangun ruang


# volume bangun balok (panjang * lebar * tinggi)

panjang = 5
lebar = 7
tinggi = 10
volume_B = panjang * lebar * tinggi
print ("volume balok adalah", volume_B)

# volume bangun kubus (sisi * sisi * sisi)

sisi1 = 20
sisi2 = 20
sisi3 = 20
volume_K = sisi1 * sisi2 * sisi3
print ("volume kubus adalah", volume_K)

# volume bangun limas {1/3 * luas alas * tinggi)

luasalas = 30
tinggi = 10
volume_L = luasalas * tinggi / 3
print ("volume limas adalah", volume_L)

# volume bangun tabung (phi * r * r * t)

phi = 22/7
ruas = 21
tinggi = 15
volume_T = ruas * ruas * tinggi * phi
print ("volume tabung adalah", volume_T)

